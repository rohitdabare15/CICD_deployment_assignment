from flask import Flask, jsonify
import os

app = Flask(__name__)

@app.route("/")
def home():
    return jsonify({
        "message": "Flask Backend Running Successfully",
        "status": "healthy"
    })

@app.route("/health")
def health():
    return jsonify({"status": "Backend OK"}), 200

if __name__ == "__main__":
    app.run(host="0.0.0.0", port=5000)